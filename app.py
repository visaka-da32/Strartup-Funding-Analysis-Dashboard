@app.callback(
    Output("kpi-total", "children"),
    Output("kpi-deals", "children"),
    Output("kpi-avg", "children"),
    Input("filtered-store", "data")
)
def update_kpis(filtered_data):
    if not filtered_data:
        k_total = "$ 0"
        k_deals = "0"
        k_avg = "$ 0"
    else:
        d = pd.DataFrame(filtered_data)
        total_funding = d["Amount in USD"].sum()
        total_deals = d["Deal Count"].sum()
        avg_deal = total_funding / total_deals if total_deals else 0

        k_total = f"${total_funding:,.0f}"
        k_deals = f"{total_deals:,}"
        k_avg = f"${avg_deal:,.0f}"

    return (
        [html.H4("Total Funding (USD)", className="text-secondary"), html.H2(k_total, className="text-success")],
        [html.H4("Number of Deals", className="text-secondary"), html.H2(k_deals, className="text-info")],
        [html.H4("Average Deal Size", className="text-secondary"), html.H2(k_avg, className="text-warning")]
    )

