# --- 3.1 For Tab 4: Resilience Index ---
def calc_resilience(df_slice):
    sector_year = (
        df_slice
        .groupby(['Industry Vertical', 'Year'], as_index=False)['Amount in USD']
        .sum()
    )
    stats = (
        sector_year
        .groupby('Industry Vertical')['Amount in USD']
        .agg(['mean', 'std', 'count'])
        .reset_index()
    )
    stats = stats[stats['count'] >= 2].copy()
    stats['Resilience Index'] = 1 - (stats['std'] / stats['mean'].replace(0, np.nan))
    stats['Resilience Index'] = stats['Resilience Index'].replace([np.inf, -np.inf], np.nan)
    stats['Resilience Index'] = stats['Resilience Index'].fillna(-1)
    stats = stats.sort_values('Resilience Index', ascending=False)
    return stats.reset_index(drop=True)

# --- 3.2 For Tab 5: Top Investors ---
def get_top_investors(df_slice, top_n=15):
    investor_list = []
    investors_data = df_slice[
        (df_slice['Investors Name'] != 'Undisclosed') &
        (df_slice['Investors Name'] != '')
    ]['Investors Name']
    for investor_string in investors_data:
        investors = [i.strip() for i in investor_string.split(',') if i.strip()]
        investor_list.extend(investors)
    investor_counts = pd.Series(investor_list).value_counts().reset_index()
    investor_counts.columns = ['Investor Name', 'Number of Investments']
    top_investors = investor_counts.head(top_n).copy()
    return top_investors.iloc[::-1].reset_index(drop=True)

# --- 3.3 For Tab 1 (Upgraded Trend Chart) ---
def get_funding_trend(df_slice):
    df_valid = df_slice[df_slice['Year'] > 0].copy()
    funding_by_year = df_valid.groupby('Year')['Amount in USD'].sum().reset_index()
