@app.callback(
    Output("funding-trend-line-upgraded", "figure"),
    Input("filtered-store", "data")
)
def update_trend_chart(filtered_data):
    if not filtered_data:
        return create_empty_fig()

    dff = pd.DataFrame(filtered_data)
    funding_stats = get_funding_trend(dff)

    if funding_stats.empty:
        return create_empty_fig()

    fig = px.line(
        funding_stats,
        x='Year',
        y='Total Funding (Billions USD)',
        text='Total Funding (Billions USD)',
        title=f"Total Funding Amount Invested Per Year",
        labels={'Total Funding (Billions USD)': 'Total Funding (Billions USD)', 'Year': 'Year'},
        markers=True
    )
    fig.update_traces(
        line=dict(color='#00CC96', width=4),
        marker=dict(size=10, symbol='circle', line=dict(width=1, color='White')),
        texttemplate='%{y:.2f}B',
        textposition="top center",
        hovertemplate='%{x}<br>Funding: %{y:.2f}B<extra></extra>'
    )
    fig.update_layout(
        template='plotly_dark', title_x=0.5, hovermode="x unified",
        xaxis_title="Year", yaxis_title="Total Funding (Billions USD)"
    )
    fig.update_xaxes(
        tickvals=funding_stats['Year'],
        tickformat='d'
    )
    return fig

